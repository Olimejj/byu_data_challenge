Create Table students(
	id int,
	last_name VARCHAR(20),
	first_name VARCHAR(20),
	adress VARCHAR(25),
	personal_email VARCHAR(20),
	work_email VARCHAR(20),
	sex char(1),
	birth DATE,
	phone_number VARCHAR(20),
	religion VARCHAR(20),
	ward VARCHAR(20),
	stake VARCHAR(20),
	high_school VARCHAR(20),
	high_school_gpa VARCHAR(20),
	act VARCHAR(20),
	sat VARCHAR(20)
	);
